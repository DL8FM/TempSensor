#include "Timer0.h"
#include "main.h"
 

/*******************************************************************************
 * Local Variables
 *******************************************************************************/
volatile unsigned int Timer0ms;


/*******************************************************************************
 * TimerInit
 * Param: void
 * Return: void
 *******************************************************************************/
void Timer0Init(void) {
	Timer0ms = 0;			// Define Timer0 variable
	TCCR0A = (1 << WGM01);		// Set Timer Mode to CTC
	OCR0A = 124;		// Set the vaue that you want to count to
	TIMSK0 = (1 << OCIE0A);		// Enable interrupt
	TCCR0B = (1 << CS01) | (1  << CS00);		// Set prescaler
}
